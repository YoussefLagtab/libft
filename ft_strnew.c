#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	int		i;
	char	*str;

	if ((str = (char*)malloc(size + 1)) == NULL)
		return (NULL);
	i = 0;
	while (i < size)
	{
		str[i] = '\0';
		i++;
	}
	return (str);
}
